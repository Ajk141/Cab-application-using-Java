    public void travel(String id)
    {
        float endtime,time;
        double amount;
        for(greencabs gre:green)
        {
       if(gre.getDt().contentEquals(id))
         {
          String et = JOptionPane.showInputDialog(null, "Enter the end time of journey");
          endtime= Float.parseFloat(et);
          time = gre.getTime();
          System.out.println("Endtime " +endtime);
          amount = 20*(endtime - time - (endtime -time)* 0.4);
          gre.setAmt(amount);
          }     
           }
    }
