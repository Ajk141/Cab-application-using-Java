    public void calc()
    {
        DisplayOfDay dd= new DisplayOfDay();
        double sum =0;
        float count =0;
        for(greencabs gre:green)
        {
           gre.display();
          sum = sum+gre.amt;
          dd.setTotal(sum);
            switch (gre.drvr) {
                case "Sam" -> {
                    dd.amt[0]+=gre.amt;
                    dd.count[0]++;
                }
                case "Alex" -> {
                    dd.amt[1]+=gre.amt;
                    dd.count[1]++;
                }
                case "Rudolf" -> {
                    dd.amt[2]+=gre.amt;
                    dd.count[2]++;
                }
                default -> {
                    dd.amt[3]+=gre.amt;
                    dd.count[3]++;
                }
            }
            count++;
            dd.setJobs(count);
        }
        dd.getdata();
        dd.display();
        dd.setVisible(true);
        this.setVisible(false);
    }
    
    
    
   

